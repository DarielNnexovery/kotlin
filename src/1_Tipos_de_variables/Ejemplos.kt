package `1_Tipos_de_variables`

fun main() {
    // todas las variables val son inmutables
    // variable inmutable de tipo entero
    val edad: Int
    edad = 48
    //variable inmutable de tipo decimal
    val sueldo: Float
    sueldo = 1200.55f // siempre lleva una f al final del valor
    //variable inmutable de tipo decimal
    val total: Double
    total = 70000.24
    //variable inmutable de tipo alfanumerico
    val titulo: String
    titulo = "sistema deventa"
    //variables mutable, las variables mutables empiezan con "var"
    var mes: Int
    mes = 1
    //algunas lineas mas de codigo
    mes = 2
    // variables de tipo Char, almacena un solo caracter
    var continua: Char = 'S'
    // variable de tipo Booleano, true o false
    var fin:Boolean = false

    print("Dariel")
    print(" Alberto")

    println(" Ulate")
    println(" Lara")

    print("mes")
}